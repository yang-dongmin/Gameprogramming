void make_treasure(int tx[], int ty[], int map_x, int map_y, int star)
{
	int total = map_x * map_y;
	int allx[400], ally[400];
	int i, j, idx = 0;

	// ëª¨ë“  ì¢Œí‘œ ì±„ìš°ê¸°
	for (i = 1; i <= map_x; i++)
		for (j = 1; j <= map_y; j++)
		{
			allx[idx] = i;
			ally[idx] = j;
			idx++;
		}

	// Fisherâ€“Yates ì…”í”Œ
	for (i = total - 1; i > 0; i--)
	{
		int r = rand() % (i + 1);
		int tmpx = allx[i];
		int tmpy = ally[i];
		allx[i] = allx[r];
		ally[i] = ally[r];
		allx[r] = tmpx;
		ally[r] = tmpy;
	}

	// ì•ìª½ starê°œë§Œ ë³µì‚¬
	for (i = 0; i < star; i++)
	{
		tx[i] = allx[i];
		ty[i] = ally[i];
	}
}

